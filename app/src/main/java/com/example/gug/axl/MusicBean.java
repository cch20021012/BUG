package com.example.gug.axl;

public class MusicBean {
    private int image;

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    @Override
    public String toString() {
        return "MusicBean{" +
                "image=" + image +
                '}';
    }
}
